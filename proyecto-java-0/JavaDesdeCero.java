package paquete;

class JavaDesdeCero {
	public static void main(String[] args) {
	    
	    boolean booleano = true;
        byte 	byte1 = 48;
        short	corto = 1000;
        int 	entero = 4000000;
        long 	largo = 1234567890;
        float 	flotante = 3.9f;
        double 	doble = 32.49d;
        char 	caracter = 'J';
        String  cadena = "'Este es un String'";

		System.out.println("Tipos de datos:");
		System.out.println("Boolean: " + booleano);
		System.out.println("Byte: " + byte1);
		System.out.println("Short: " + corto);
		System.out.println("Integer: " + entero);
		System.out.println("Long: " + largo);
		System.out.println("Float " + flotante);
		System.out.println("Double: " + doble);
		System.out.println("Character: " + caracter);
		System.out.println("String: " + cadena);
	}
}
